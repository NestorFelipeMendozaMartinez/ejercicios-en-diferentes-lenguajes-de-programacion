# Definición de las variables
a = 10
b = 4

# Multiplicación
puts "La primera variable es: #{a}"
puts "El signo de la operación es: *"
puts "La segunda variable es: #{b}"
c = a * b
puts "El resultado es: #{c}"
puts "El tipo de c es: #{c.class}"

# División
puts "La primera variable es: #{a}"
puts "El signo de la operación es: /"
puts "La segunda variable es: #{b}"
c = a / b
puts "El resultado es: #{c}"
puts "El tipo de c es: #{c.class}"

# Suma
puts "La primera variable es: #{a}"
puts "El signo de la operación es: +"
puts "La segunda variable es: #{b}"
