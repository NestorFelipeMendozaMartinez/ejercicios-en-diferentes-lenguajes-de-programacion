# Definir las variables booleanas
a = true
b = false

# Operación lógica 'and'
puts a && b

# Definir variables numéricas
x = 2
y = 3
z = 4
w = 10

# Operación lógica con comparación '==' y '<' y '>'
puts (x == y) && (z < w)

# Operación lógica con 'not' (invertir una condición)
puts !(x == y) && (z > w)