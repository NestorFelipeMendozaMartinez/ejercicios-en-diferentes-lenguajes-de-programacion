# Crear un "diccionario" utilizando un Hash en Ruby
coche = {}

# Agregar elementos al Hash
coche["marca"] = "Ford"
coche["color"] = "rojo"
coche["modelo"] = "sedan"
coche["placa"] = "ROS227"

# Mostrar el valor de 'color'
puts coche["color"]

# Cambiar el valor de 'color'
coche["color"] = "verde"
puts coche["color"]

# Mostrar el valor de 'marca'
puts coche["marca"]

# Cambiar el valor de 'marca'
coche["marca"] = "Renault"
puts coche["marca"]

# Mostrar todo el Hash
puts coche